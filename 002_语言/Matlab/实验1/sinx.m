x=-2*pi:0.1:2*pi;
y=sin(x);
y1=x;
y2=y1-x.^3./factorial(3);
y3=y2+x.^5./factorial(5);
y4=y3-x.^7./factorial(7);
y5=y4+x.^9./factorial(9);
y6=y5-x.^11./factorial(11);
y7=y6+x.^13./factorial(13);
y8=y7-x.^15./factorial(15);
subplot(2,4,1);
plot(x,y1,x,y);
subplot(2,4,2);
plot(x,y2,x,y);
subplot(2,4,3);
plot(x,y3,x,y);
subplot(2,4,4);
plot(x,y4,x,y);
subplot(2,4,5);
plot(x,y5,x,y);
subplot(2,4,6);
plot(x,y6,x,y);
subplot(2,4,7);
plot(x,y7,x,y);
subplot(2,4,8);
plot(x,y8,x,y);